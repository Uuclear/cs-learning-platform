#include <stdio.h>

/**
 * C语言入门：Hello World 程序详解
 *
 * 这是最经典的C程序，展示了C语言的基本结构：
 * 1. #include 预处理指令 - 包含标准输入输出头文件
 * 2. main 函数 - 程序的入口点
 * 3. printf 函数 - 输出字符串到控制台
 * 4. return 0 - 表示程序正常退出
 */

int main() {
    // 使用printf函数输出"Hello, World!"到控制台
    printf("Hello, World!\n");

    // 返回0表示程序成功执行完毕
    return 0;
}