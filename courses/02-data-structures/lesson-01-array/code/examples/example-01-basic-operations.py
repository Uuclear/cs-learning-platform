# ============================================
# 示例1：数组的基本操作演示
# 就像操作一排带编号的小盒子
# ============================================

# Python的列表（list）就是动态数组
fruits = ["苹果", "香蕉", "橙子", "葡萄", "西瓜"]

print("=== 原始数组 ===")
print(f"水果列表: {fruits}")
print(f"数组长度: {len(fruits)}")
print()

# 1. 访问元素 - O(1)，超快！
print("=== 访问元素 ===")
print(f"第0个水果（第一个）: {fruits[0]}")   # 索引从0开始！
print(f"第2个水果: {fruits[2]}")
print(f"最后一个水果: {fruits[-1]}")       # Python支持负数索引
print()

# 2. 修改元素 - O(1)，也是瞬间完成！
print("=== 修改元素 ===")
fruits[1] = "芒果"  # 把香蕉换成芒果
print(f"把香蕉换成芒果后: {fruits}")
print()

# 3. 插入元素 - O(n)，要移动后面的元素
print("=== 插入元素 ===")
print(f"插入前: {fruits}")
fruits.insert(2, "草莓")  # 在索引2的位置插入草莓
print(f"在位置2插入草莓后: {fruits}")
print("注意：橙子、葡萄、西瓜都往后挪了一位！")
print()

# 4. 删除元素 - O(n)，也要移动元素
print("=== 删除元素 ===")
print(f"删除前: {fruits}")
removed = fruits.pop(3)  # 删除索引3的元素
print(f"删除的元素是: {removed}")
print(f"删除后: {fruits}")
print("注意：后面的元素都往前补位了！")
print()

# 5. 在末尾添加 - 平均O(1)，通常是很快的
print("=== 在末尾添加 ===")
fruits.append("桃子")
print(f"添加桃子后: {fruits}")

