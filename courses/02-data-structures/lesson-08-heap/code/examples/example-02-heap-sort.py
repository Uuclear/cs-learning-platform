# 示例2：堆排序算法
# 利用堆的性质实现O(n log n)的排序算法

def build_max_heap(arr):
    """
    把无序数组构建成最大堆
    从最后一个非叶子节点开始，依次下沉
    时间复杂度：O(n) - 神奇！不是O(n log n)
    """
    n = len(arr)
    # 最后一个非叶子节点的索引是 (n//2) - 1
    for i in range(n // 2 - 1, -1, -1):
        sift_down(arr, n, i)


def sift_down(arr, n, i):
    """
    下沉操作：将索引i的元素向下调整
    arr: 数组
    n: 堆的有效大小
    i: 当前节点索引
    """
    largest = i
    left = 2 * i + 1   # 左子节点
    right = 2 * i + 2  # 右子节点

    # 如果左子节点比当前节点大
    if left < n and arr[left] > arr[largest]:
        largest = left

    # 如果右子节点比当前节点大
    if right < n and arr[right] > arr[largest]:
        largest = right

    # 如果最大值不是当前节点，交换并继续下沉
    if largest != i:
        arr[i], arr[largest] = arr[largest], arr[i]
        sift_down(arr, n, largest)


def heap_sort(arr):
    """
    堆排序算法
    1. 构建最大堆
    2. 反复把堆顶（最大值）和末尾交换
    3. 缩小堆的范围，重新调整
    时间复杂度：O(n log n)
    空间复杂度：O(1) - 原地排序！
    """
    n = len(arr)

    # 第一步：构建最大堆
    build_max_heap(arr)
    print(f"构建最大堆后: {arr}")

    # 第二步：反复提取最大值
    for i in range(n - 1, 0, -1):
        # 把堆顶（最大值）和当前末尾交换
        arr[0], arr[i] = arr[i], arr[0]
        # 缩小堆的范围，对新的堆顶下沉
        sift_down(arr, i, 0)
        print(f"  提取最大值后: {arr}")

    return arr


def heap_sort_visual(arr):
    """带可视化步骤的堆排序"""
    print(f"原始数组: {arr}")
    print(f"数组长度: {len(arr)}\n")

    result = arr.copy()
    sorted_result = heap_sort(result)

    print(f"\n排序结果: {sorted_result}")
    return sorted_result


