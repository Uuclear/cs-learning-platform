# 示例3: BFS（广度优先搜索）演示
# 队列的经典应用：像水波纹一样逐层扩散

from collections import deque


def bfs(graph, start):
    """
    广度优先搜索（Breadth-First Search）
    就像往池塘扔石头，水波纹一圈一圈往外扩散

    参数:
        graph: 邻接表表示的图 {节点: [相邻节点列表]}
        start: 起始节点

    返回:
        访问顺序列表
    """
    # 记录已访问的节点
    visited = set()
    # 结果列表（访问顺序）
    result = []
    # 用双端队列作为 BFS 的队列
    queue = deque([start])
    visited.add(start)

    print(f"  初始: 队列 = {list(queue)}, 已访问 = {visited}")

    while queue:
        # 从队首取出一个节点
        node = queue.popleft()
        result.append(node)
        print(f"  处理节点 '{node}' → 结果: {result}")

        # 把这个节点的所有未访问邻居加入队列
        for neighbor in graph.get(node, []):
            if neighbor not in visited:
                visited.add(neighbor)
                queue.append(neighbor)
                print(f"    邻居 '{neighbor}' 入队 → 队列: {list(queue)}")

        print(f"  本轮结束: 队列 = {list(queue)}\n")

    return result


def demonstrate_bfs():
    """演示BFS算法"""
    print("=== BFS（广度优先搜索）演示 ===\n")

    # 构建一个社交网络图（谁认识谁）
    # 用邻接表表示
    social_network = {
        "张三": ["李四", "王五", "赵六"],
        "李四": ["张三", "王五", "孙七"],
        "王五": ["张三", "李四", "周八"],
        "赵六": ["张三", "周八"],
        "孙七": ["李四", "郑九"],
        "周八": ["王五", "赵六", "郑九"],
        "郑九": ["孙七", "周八"],
    }

    print("社交网络关系:")
    for person, friends in social_network.items():
        print(f"  {person} 认识: {', '.join(friends)}")
    print()

    # 从"张三"开始BFS（看看他的朋友圈有多大）
    print("--- 从张三开始扩散 ---\n")
    visit_order = bfs(social_network, "张三")

    print("=" * 50)
    print(f"BFS访问顺序: {' → '.join(visit_order)}")
    print(f"总共访问了 {len(visit_order)} 个人")
    print("这就是'六度分隔理论'的基础——通过队列，我们逐层探索社交网络！")


# 运行演示
if __name__ == "__main__":
    demonstrate_bfs()

