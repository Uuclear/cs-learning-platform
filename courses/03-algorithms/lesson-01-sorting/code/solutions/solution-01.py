def selection_sort(arr):
    """
    选择排序
    参数: arr - 待排序的列表
    返回: 排序后的列表
    """
    n = len(arr)
    for i in range(n):
        # 假设当前位置是最小值
        min_idx = i
        # 在剩余部分中寻找真正的最小值
        for j in range(i + 1, n):
            if arr[j] < arr[min_idx]:
                min_idx = j
        # 交换当前位置和最小值位置
        if min_idx != i:
            arr[i], arr[min_idx] = arr[min_idx], arr[i]
        print(f"  第{i+1}轮: 最小值 {arr[i]} 放在位置 {i}, 数组: {arr}")
    return arr


if __name__ == "__main__":
    print("【选择排序演示】")
    test = [29, 10, 14, 37, 13]
    print(f"原始数组: {test}")
    selection_sort(test)
    print(f"排序结果: {test}")


