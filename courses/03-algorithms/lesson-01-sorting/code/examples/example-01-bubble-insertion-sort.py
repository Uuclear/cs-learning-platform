# 示例1：简单排序算法 —— 冒泡排序与插入排序
# 核心思想：用最朴素的方式，一步步把数组排好序

def bubble_sort(arr):
    """
    冒泡排序
    核心：每次比较相邻两个元素，把大的往后"冒泡"
    就像气泡水里的气泡，慢慢浮到水面
    """
    n = len(arr)
    for i in range(n):
        # 每轮把最大的元素"冒"到末尾
        # 已经排好的部分不需要再比较
        swapped = False  # 优化：如果一轮没交换，说明已经有序
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        print(f"  第{i+1}轮后: {arr}")
        if not swapped:
            print(f"  -> 提前结束！数组已有序")
            break
    return arr


def insertion_sort(arr):
    """
    插入排序
    核心：把新元素插入到已经排好序的部分中
    就像打扑克时，拿到一张新牌，插到合适的位置
    """
    for i in range(1, len(arr)):
        key = arr[i]  # 当前要插入的元素
        j = i - 1
        # 把比key大的元素往后移一位
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key  # 插入到正确位置
        print(f"  插入{key}后: {arr}")
    return arr


if __name__ == "__main__":
    print("=" * 40)
    print("【冒泡排序演示】")
    test_arr1 = [64, 34, 25, 12, 22, 11, 90]
    print(f"原始数组: {test_arr1}")
    bubble_sort(test_arr1.copy())  # 用copy避免修改原数组
    print(f"排序结果: {sorted(test_arr1)}")  # 用内置sorted验证结果

    print("\n" + "=" * 40)
    print("【插入排序演示】")
    test_arr2 = [12, 11, 13, 5, 6]
    print(f"原始数组: {test_arr2}")
    insertion_sort(test_arr2.copy())
    print(f"排序结果: {sorted(test_arr2)}")


