# 斐波那契数列：递归 vs 记忆化 vs 迭代
# 这是学习动态规划最好的入门案例，就像学游泳前先学憋气

import time


# ============================================
# 方法1：朴素递归（反面教材）
# ============================================

def fib_recursive(n):
    """
    朴素递归实现斐波那契数列
    F(0)=0, F(1)=1, F(n)=F(n-1)+F(n-2)
    问题：大量重复计算！算F(5)时F(3)被算2次，F(2)被算3次
    """
    # 基准情况：F(0)和F(1)已知
    if n <= 1:
        return n

    # 递归：F(n) = F(n-1) + F(n-2)
    return fib_recursive(n - 1) + fib_recursive(n - 2)


# ============================================
# 方法2：记忆化搜索（自顶向下的DP）
# ============================================

def fib_memo(n, memo=None):
    """
    记忆化搜索实现斐波那契数列
    核心思想：算过一次的结果记在小本本上，下次直接用
    就像你被问"3×7等于几"——算过一遍就记住了，不用重算
    """
    # 初始化记忆本（第一次调用时创建）
    if memo is None:
        memo = {}

    # 先查记忆本：算过就直接返回
    if n in memo:
        return memo[n]

    # 基准情况
    if n <= 1:
        return n

    # 计算并记录结果
    memo[n] = fib_memo(n - 1, memo) + fib_memo(n - 2, memo)
    return memo[n]


# ============================================
# 方法3：迭代法（自底向上的DP）
# ============================================

def fib_iterative(n):
    """
    迭代法实现斐波那契数列
    核心思想：从最小的子问题开始，一步步往上算
    就像盖楼，先打地基，再一层一层往上盖
    """
    # 基准情况
    if n <= 1:
        return n

    # 只需要保存前两个值（状态压缩）
    prev2 = 0  # F(n-2)
    prev1 = 1  # F(n-1)

    # 从F(2)开始，一步步算到F(n)
    for i in range(2, n + 1):
        current = prev1 + prev2  # F(n) = F(n-1) + F(n-2)
        prev2 = prev1            # 更新F(n-2)
        prev1 = current          # 更新F(n-1)

    return prev1


